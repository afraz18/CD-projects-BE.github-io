<script>
  (function(){
    const sel = document.getElementById('roleSelect');
    const cards = document.querySelectorAll('.role-card');
    function showRole(val){
      if(val === 'all'){
        cards.forEach(c => { c.style.display = ''; });
      } else {
        cards.forEach(c => {
          c.style.display = c.getAttribute('data-role') === val ? '' : 'none';
        });
        // scroll into view for better UX
        const visible = document.querySelector('.role-card[style*="display:"]:not([style*="display: none"])');
        if(visible){
          visible.scrollIntoView({behavior:'smooth', block:'start'});
        }
      }
    }
    // on load - show all
    showRole('all');
    sel.addEventListener('change', (e) => showRole(e.target.value));
  })();
</script>


